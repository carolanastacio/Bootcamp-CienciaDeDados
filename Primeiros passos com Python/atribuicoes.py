saldo+= 10  #com adição
print (saldo)

saldo-= 50  #com subtração
print (saldo)

saldo/= 10  #com divisão
print (saldo)

saldo*= 10  #com multiplicação
print (saldo)

saldo*= 10  #com exponenciação
print (saldo)